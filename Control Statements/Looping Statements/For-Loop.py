# Looping through a list of numbers
numbers = [1, 2, 3, 4, 5]
for num in numbers:
    print("Number:", num)

# Looping through a list of fruits
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print("I like", fruit)

# Print numbers from 1 to 5
for i in range(1, 6):
    print("Number:", i)
